    public function base64_encode($data)
    {
        if (!$this->is_base64_encoded($data)) {
            return base64_encode($data);
        }
        return $data;
    }


    public function isString($str)
    {
        if ($str !== null) {
            if (is_string($str)) {
                if (strlen($str) > 0) {
                    return true;
                }
            }
        }
        return false;
    }

    public function utf8($string)
    {
        if ($this->isString($string)) {
            $string = iconv(mb_detect_encoding($string, "UTF-8,ISO-8859-1"), "UTF-8", $string);
            return $string;
        }
        return null;
    }

    public function base64_decode($data)
    {
        if ($this->is_base64_encoded($data)) {
            return base64_decode($data);
        }
        return $data;
    }

    function is_base64_encoded($s)
    {
        // Check if there are valid base64 characters
        if (!preg_match('/^[a-zA-Z0-9\/\r\n+]*={0,2}$/', $s)) return false;

        // Decode the string in strict mode and check the results
        $decoded = base64_decode($s, true);
        if (false === $decoded) return false;

        // Encode the string again
        if (base64_encode($decoded) != $s) return false;

        return true;
    }

    public function uppercase($str)
    {
        $string = $str;
        if ($this->is_string($str)) {
            $string = mb_convert_case($string, MB_CASE_UPPER, "UTF-8");
            return $string;
        }
        return null;
    }

    public function lowercase($str)
    {
        $string = $str;
        if ($this->is_string($str)) {
            $string = mb_convert_case($string, MB_CASE_LOWER, "UTF-8");
            return $string;
        }
        return null;
    }

    public function is_string($str)
    {
